tempo = simOut(1,11).tout(1:86401);

% Create seconds object 
secondi = seconds(tempo = simOut(1,11).tout(1:86401));

% Create datetime object
tempo1 = datetime(datetime('today') + secondi, 'Format', 'HH:mm:ss');

power = simOut(1,11).TEG_power.signals.values(345601:end);

delta=simOut(1,11).TEG_dt.signals.values(345601:end);

voltage=simOut(1,11).TEG_voltage.signals.values(345601:end);

subplot(3,1,1)
grid on 
plot(tempo1, power, 'LineWidth', 6)
title('Electrical power generated by TEGs as a function of time')
xlabel('Time [s]')
ylabel('Power [W]')
fontsize(25,"points")

subplot(3,1,2)
grid on
plot(tempo1, voltage, 'LineWidth', 6)
title('Electrical voltage generated by TEGs as a function of time')
xlabel('Time [s]')
ylabel('Voltage [V]')
fontsize(25,"points")

